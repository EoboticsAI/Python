"""
Binary to Decimal Converter using Python's built-in int() function.

This script takes a binary (base-2) number as input from the user
and prints its decimal (base-10) equivalent. It uses the int() function
with base 2 to convert the binary string to an integer.

Usage:
    Run the script and enter a valid binary number when prompted.

Example:
    Input: 1010
    Output: Decimal is: 10
"""

# Binary Number to Decimal Number Converter without using loops

binary_num = input("Enter Binary No: ")
